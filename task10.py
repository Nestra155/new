'''
Дана строка. Замените в этой строке все появления буквы h на букву H, 
кроме первsого и поsсeледнего вхождения.
'''
# Дана строка
s = "hello world, hhhh how are you?"

# Находим первое вхождение буквы 'h'
first_h = s.find('h')

# Находим последнее вхождение буквы 'h'
last_h = s.rfind('h')

# Заменяем все 'h' на 'H', кроме первого и последнего вхождения
result = s[:first_h+1] + s[first_h+1:last_h].replace('h', 'H') + s[last_h:]

print(result)